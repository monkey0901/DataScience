package operator;

public class Demo03 {
    public static void main(String[] args) {
        // 关系运算符：> < == !=
        // 返回结果：true false 布尔值
        int a = 10;
        int b = 20;
        int c = 24;

        //取余，模运算
        System.out.println(c%a);

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
